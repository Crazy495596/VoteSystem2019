package Model;

public class VoteMessage {
	private Integer messageId;
	private Integer votesum;
	private String messMusic;
	private String pkName;
	private String addtime;
	private String singerName;
	public Integer getMessageId() {
		return messageId;
	}
	public void setMessageId(Integer messageId) {
		this.messageId = messageId;
	}
	public Integer getVotesum() {
		return votesum;
	}
	public void setVotesum(Integer votesum) {
		this.votesum = votesum;
	}
	public String getMessMusic() {
		return messMusic;
	}
	public void setMessMusic(String messMusic) {
		this.messMusic = messMusic;
	}
	public String getPkName() {
		return pkName;
	}
	public void setPkName(String pkName) {
		this.pkName = pkName;
	}
	public String getAddtime() {
		return addtime;
	}
	public void setAddtime(String addtime) {
		this.addtime = addtime;
	}
	public String getSingerName() {
		return singerName;
	}
	public void setSingerName(String singerName) {
		this.singerName = singerName;
	}
	
	
	
	
	
	

}
